    public decimal Lookup(int numChildren, decimal combinedIncome, string ageGroup)
    {
        if (numChildren < 1) numChildren = 1;
        if (numChildren > 6) numChildren = 6;

        // Cap for table lookup; extended formula handles above-table income
        var tableMaxIncome = 18000m;

        if (combinedIncome > tableMaxIncome)
        {
            return ExtendedFormula(numChildren, combinedIncome, ageGroup);
        }

        var key = (numChildren, ageGroup);
        if (!Tables.TryGetValue(key, out var table))
            return ExtendedFormula(numChildren, combinedIncome, ageGroup);

        // Exact match
        if (table.ContainsKey(combinedIncome))
            return table[combinedIncome];

        // Linear interpolation
        var keys = table.Keys;
        if (combinedIncome < keys[0])
        {
            // Below table minimum — extrapolate from first two rows proportionally
            return table.Values[0] * combinedIncome / keys[0];
        }

        // Find surrounding rows
        decimal lowerKey = keys[0], upperKey = keys[0];
        decimal lowerVal = table.Values[0], upperVal = table.Values[0];
        for (int i = 0; i < keys.Count - 1; i++)
        {
            if (keys[i] <= combinedIncome && keys[i + 1] >= combinedIncome)
            {
                lowerKey = keys[i];
                upperKey = keys[i + 1];
                lowerVal = table.Values[i];
                upperVal = table.Values[i + 1];
                break;
            }
        }

        if (lowerKey == upperKey) return lowerVal;

        var fraction = (combinedIncome - lowerKey) / (upperKey - lowerKey);
        var result = lowerVal + fraction * (upperVal - lowerVal);
        return Math.Round(result, MidpointRounding.AwayFromZero);
    }

    public decimal ExtendedFormula(int numChildren, decimal combinedIncome, string ageGroup)
    {
        if (numChildren < 1) numChildren = 1;
        if (numChildren > 6) numChildren = 6;

        if (!ExtendedMultipliers.TryGetValue(numChildren, out var multiplier))
            multiplier = ExtendedMultipliers[1];

        // Base formula: income^0.6386 × multiplier (for age 12-18)
        var base1218 = (decimal)(Math.Pow((double)combinedIncome, 0.6386) * multiplier);

        return ageGroup switch
        {
            "12-18" => Math.Round(base1218, MidpointRounding.AwayFromZero),
            "6-11" => Math.Round(base1218 * 0.94m, MidpointRounding.AwayFromZero),
            "0-5" => Math.Round(base1218 * 0.84m, MidpointRounding.AwayFromZero),
            _ => Math.Round(base1218, MidpointRounding.AwayFromZero)
        };
    }
